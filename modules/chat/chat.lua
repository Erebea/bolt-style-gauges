        local _, y = event:vertexxy(i + 2)
        barindex = i
        bararrowtop = y
      end
      ::continue::
    end
    if barindex == nil then return nil end

    -- try to find the first timestamp
    local firstcharindex = nil
    local timestampx
    local timestampy
    for i = startindex, barindex - (verticesperimage * 4), verticesperimage do
      local ax, ay, aw, ah, _, _ = event:vertexatlasdetails(i)
      if this:chatindexcouldbetimestamp(event, i, ax, ay, aw, ah) then
        local x, y = event:vertexxy(i + 2)
        firstcharindex = i
        timestampx = x
        timestampy = y
        break
      end
    end
    if firstcharindex == nil then return nil end

    -- build a list of new messages, one character at a time, starting a new one when we find a new timestamp
    local newmessages = {}
    local newmessagecount = 0
    local msg = ""
    local msgexists = false
    local lastx
    local lasty
    local lastax
    local lastay
    for i = firstcharindex, barindex - (verticesperimage * 4), verticesperimage do
      local x, y = event:vertexxy(i + 2)
      local ax, ay, aw, ah, _, _ = event:vertexatlasdetails(i)
      if lastx and math.abs(x - lastx) < 2 and math.abs(y - lasty) < 2 and ax == lastax and ay == lastay then goto continue end
