          newmessagecount = newmessagecount + 1
          newmessages[newmessagecount] = msg
          msg = ""
          msgexists = false
        end
        timestampy = y
      end
      if y >= timestampy then
        local c = this:lookupchatcharacter(event, ax, ay, aw, ah)
        if c then
          msg = msg .. c
          msgexists = true
        end
      end
      ::continue::
    end
